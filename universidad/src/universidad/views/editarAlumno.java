    /**
     * Metodo creado que busca los dni de los alumnos y los carga
     * automaticamente en un ComboBox para ser reutilizados nuevamente para
     * editar el alumno a posterior.
     *
     * @throws IOException
     * @throws SQLException
     */
    public final void cargarDNIAlumnos() throws IOException, SQLException {
        DefaultComboBoxModel comboBox = new DefaultComboBoxModel();
        cmbDNI.setModel(comboBox);
        ArrayList<Alumno> alumnos;
        alumnos = alumnoData.selectAlumnos(0);
        for (Alumno alumno1 : alumnos) {
            comboBox.addElement(alumno1.getDni());
        }

    }

    /**
     * Metodo para setar los datos extraidos desde un combo box que recibe dni
     * de alumnos
     */
    public void setearDatos() {
        Date date = Date.from(alumno.getFecha_nacimiento().atStartOfDay(ZoneId.systemDefault()).toInstant());
        txtEditarApellido.setText(alumno.getApellido());
        txtEditarNombre.setText(alumno.getNombre());
        editarFechaNacimiento.setDate(date);
        rbtnEditarActivar.setSelected(alumno.isEstado());
    }

