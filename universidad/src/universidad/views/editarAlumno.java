    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        try {
            Date fechaSeleccionada = editarFechaNacimiento.getDate();
            Instant instante = fechaSeleccionada.toInstant();
            ZoneId zona = ZoneId.systemDefault();
            ZonedDateTime zonedDateTime = instante.atZone(zona);
            LocalDate fechaNac = zonedDateTime.toLocalDate();
            alumno.setApellido(txtEditarApellido.getText());
            alumno.setNombre(txtEditarNombre.getText());
            alumno.setEstado(rbtnEditarActivar.isSelected());
            alumno.setFecha_nacimiento(fechaNac);
            alumnoData.udateAlumno(alumno);
            Home home = new Home();
            home.mostrarTodosAlumnos();

            JOptionPane.showMessageDialog(null, "Alumno correctamente actualizado");
        } catch (IOException | SQLException ex) {
            Logger.getLogger(editarAlumno.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

